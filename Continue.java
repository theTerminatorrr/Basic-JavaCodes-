public class Continue
{
    public static void main(String[] args)
    {
        for(int count = 1; count <= 3; ++count)
        {
            if (count == 2)
            {
                System.out.println("Hello ");
            }
            else
            {
                System.out.println("Google");
                System.out.println("Gemini.");
            }
        }
    }
}
